void ili9341_cmd_mode(void){
    MAP_GPIOPinWrite(GPIO_PORTE_BASE, GPIO_PIN_1, 0x00);
}
void ili9341_data_mode(void){
    MAP_GPIOPinWrite(GPIO_PORTE_BASE,GPIO_PIN_1, GPIO_PIN_1);
}

void ili9341_reset(void){
    // PE2 to low for reset.
    MAP_GPIOPinWrite(GPIO_PORTE_BASE, GPIO_PIN_2, 0x00);
    MAP_SysCtlDelay((systemClkFreq / 3000) * 5); // ~5ms
    // PE2 high
    MAP_GPIOPinWrite(GPIO_PORTE_BASE, GPIO_PIN_2, GPIO_PIN_2);
    MAP_SysCtlDelay((systemClkFreq / 3000) * 5); // ~5ms
}

void ili9341_send_command(uint8_t cmd){
    ili9341_enable();
    ili9341_cmd_mode();
    // Send the command trough SPI1
    MAP_SSIDataPut(SSI2_BASE, cmd);
    // Wait for data to be tansfered
    while(MAP_SSIBusy(SSI2_BASE));
    ili9341_disable();
}

void ili9341_send_data(uint8_t data){
    ili9341_enable();
    ili9341_data_mode();
    // Send the data to the spi2
    MAP_SSIDataPut(SSI2_BASE, data);
    // Wait for data to be tansfered
    while(MAP_SSIBusy(SSI2_BASE));
    ili9341_disable();
}

void ili9341_init(void){ 
    // Spi2 //
    spi2_config();
    // uDMA //
    uDMA_spi2_config();
    ili9341_enable();
    ili9341_reset();
    // Change to 8bit len to send commands
    spi2_data_len(8);
    ili9341_send_command(0x01); // Software reset
    MAP_SysCtlDelay((systemClkFreq / 3000) * 5); // ~5ms
    ili9341_send_command(0x11); // Sleep out
    MAP_SysCtlDelay((systemClkFreq / 3000) * 5); // ~5ms
    ili9341_send_command(0x3A); // Pixel Format
    ili9341_send_data(0x55); // RGB565
    ili9341_send_command(0x36); // MADCTL
    ili9341_send_data(0xC0); // RGB, 180 degrees rotation
    ili9341_send_command(0x29);
    ili9341_disable();
    // Change to 16 bit len for send pixels data
    spi2_data_len(16);
    MAP_SysCtlDelay((systemClkFreq / 3000) * 5); // ~5ms
}

void ili9341_set_window(uint16_t x0, uint16_t y0,uint16_t x1, uint16_t y1){
    // Config spi2 to send command and data configuration
    spi2_data_len(8);
    // Column Address Set (CASET)
    ili9341_send_command(0x2A);
    ili9341_send_data(x0 >> 8);     // X start high
    ili9341_send_data(x0 & 0xFF);   // X start low
    ili9341_send_data(x1 >> 8);     // X end high
    ili9341_send_data(x1 & 0xFF);   // X end low

    // Page Address Set (PASET)
    ili9341_send_command(0x2B);
    ili9341_send_data(y0 >> 8);     // Y start high
    ili9341_send_data(y0 & 0xFF);   // Y start low
    ili9341_send_data(y1 >> 8);     // Y end high
    ili9341_send_data(y1 & 0xFF);   // Y end low

    // Write to RAM
    ili9341_send_command(0x2C);
    // Config spi3 to send pixels data
    spi2_data_len(16);
    
}

void ili9341_fill_screen(uint16_t color){
    uint16_t  i;
    // Fill a chunk color buffer
    for(i=0;i<ILI9341_WIDTH*ILI9341_LINES_PER_BUFFER;i++)
        colorBufer[i] = color;
    
    ili9341_set_window(0, 0, ILI9341_WIDTH - 1, ILI9341_HEIGHT - 1);
    ili9341_enable();
    ili9341_data_mode();

    for (i = 0; i < (ILI9341_HEIGHT/ILI9341_LINES_PER_BUFFER); i++){
        /*
         * Wait only for DMA done — NOT for SSIBusy.
         * The FIFO keeps clocking bits out in the background while we poll.
         * The next DMA burst starts feeding the FIFO as soon as it has room,
         * with zero dead time on the SPI bus.
         */
        while (MAP_uDMAChannelSizeGet(UDMA_CH13_SSI2TX | UDMA_PRI_SELECT));

        uDMA_spi2_send_buffer(colorBufer, ILI9341_WIDTH * ILI9341_LINES_PER_BUFFER);
    }

    /* Wait for the shift register to drain only once, at the very end */
    while (SSIBusy(SSI2_BASE));
}

void ili9341_draw_char(uint16_t x, uint16_t y,char ch,uint16_t fgColor, uint16_t bgColor){
    // Usefull variables to fill the buffer
    uint8_t   row, col;
    uint8_t   fontIdx;
    uint8_t   fontByte;
    uint16_t *pPix = g_ui16CharBuf;

    // ── Bounds check ─────────────────────────────────────────────────────────
    // A character starting at x=236 would write pixels 236..243,
    // but the screen only goes to 239 → corrupts ILI9341 address counter.
    if (x + FONT_WIDTH > ILI9341_WIDTH)  return;
    if (y + FONT_HEIGHT > ILI9341_HEIGHT) return;

    // ── ASCII → font table index ─────────────────────────────────────────────
    // Your table starts at 0x20 (space), so:
    //   ' '  → index 0
    //   '!'  → index 1
    //   'A'  → index 33   (0x41 - 0x20)
    if (ch < (char)FONT_FIRST_CHAR || ch > (char)FONT_LAST_CHAR)
        ch = (char)FONT_FALLBACK;
    fontIdx = (uint8_t)((uint8_t)ch - FONT_FIRST_CHAR);

    // ── Expand bitmap → RGB565 pixels ─────────────────────────────────────────
    // Outer loop: rows top → bottom (row 0 = topmost screen row of glyph)
    // Inner loop: columns left → right
    // Memory layout in g_ui16CharBuf:
    //   [0..7]   = row 0  (top row of glyph,   8 pixels)
    //   [8..15]  = row 1
    //   ...
    //   [120..127] = row 15 (bottom row of glyph)
    // This is exactly the order ILI9341 expects after RAMWR
    // because we set a top-to-bottom window.
    for (row = 0; row < FONT_HEIGHT; row++)
    {
        fontByte = font8x16[fontIdx][row];

        for (col = 0; col < FONT_WIDTH; col++)
        {
            // Shift mask from MSB down: col0→bit7, col1→bit6 ... col7→bit0
            *pPix++ = (fontByte & (0x80u >> col)) ? fgColor : bgColor;
        }
    }

    // ── Set 8×16 window, fire DMA ─────────────────────────────────────────────
    // Window = exactly the character bounding box.
    // ILI9341 will auto-advance within this window — no address math needed.
    ili9341_set_window(x,y,x + FONT_WIDTH - 1, y + FONT_HEIGHT - 1);

    ili9341_enable();      // CS low
    ili9341_data_mode();   // DC high — pixel stream

    // Kick off DMA: 128 pixels × 16-bit = 256 bytes
    uDMA_spi2_send_buffer(g_ui16CharBuf, FONT_WIDTH * FONT_HEIGHT);

    // Must wait for BOTH DMA done AND shift register empty before
    // the next ili9341_set_window() call. set_window sends SPI commands
    // and if SSI is still shifting pixels you corrupt the pixel stream.
    while (MAP_uDMAChannelSizeGet(UDMA_CH13_SSI2TX | UDMA_PRI_SELECT));
    while (SSIBusy(SSI2_BASE));

    ili9341_disable();     // CS high
    
}

void ili9341_print_string(uint16_t x, uint16_t y,const char *str,uint16_t fgColor,uint16_t bgColor){
    uint16_t curX = x;
    uint16_t curY = y;

    while (*str != '\0')
    {
        char ch = *str++;

        // ── Newline ───────────────────────────────────────────────────────────
        if (ch == '\n')
        {
            curX  = x;          // Return to left margin (not column 0 — respects x)
            curY += FONT_HEIGHT;     // Drop one text row (16 pixels)
            if (curY + FONT_HEIGHT > ILI9341_HEIGHT) return;   // Off bottom → stop
            continue;
        }

        if (ch == '\r') continue;   // Ignore bare carriage return

        // ── Auto word-wrap ───────────────────────────────────────────────────
        // Don't let a character start where it would run past the right edge
        if (curX + FONT_WIDTH > ILI9341_WIDTH)
        {
            curX  = x;
            curY += FONT_HEIGHT;
            if (curY + FONT_HEIGHT > ILI9341_HEIGHT) return;
        }

        ili9341_draw_char(curX, curY, ch, fgColor, bgColor);
        curX += FONT_WIDTH;    // Advance cursor one glyph width
    }
} 

void ili9341_print_int(uint16_t x, uint16_t y,int32_t num,uint16_t color, uint16_t bg){
    char buf[12];
    intToStr(num, buf);
    ili9341_print_string(x,y,buf,color,bg);
}

void ili9341_print_float(uint16_t x, uint16_t y,float num, uint8_t decimals,uint16_t color, uint16_t bg){
    char buf[12];
    floatToStr(num, buf,decimals);
    ili9341_print_string(x,y,buf,color,bg);
}

// SPI2 for display //
void spi2_config(void){
    // Enabling and wait for QSSI module and ports D and E to be ready
    MAP_SysCtlPeripheralEnable(SYSCTL_PERIPH_SSI2);
    while(!MAP_SysCtlPeripheralReady(SYSCTL_PERIPH_SSI2));
    MAP_SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOD);
    while(!MAP_SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOD));
    MAP_SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOE);
    while(!MAP_SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOE));
    // Configure pins for QSSI2
    // {PD3:Clk,PE0:CS,PPD1:TX,PPD0:RX,PE2:rst,PE1:C/D}
    MAP_GPIOPinTypeGPIOOutput(GPIO_PORTD_BASE,GPIO_PIN_1 | GPIO_PIN_3);
    MAP_GPIOPinTypeGPIOOutput(GPIO_PORTE_BASE,GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_2);
    MAP_GPIOPinTypeGPIOInput(GPIO_PORTD_BASE,GPIO_PIN_0);
    MAP_GPIOPinTypeGPIOInput(GPIO_PORTE_BASE,GPIO_PIN_3);
    MAP_GPIOPinConfigure(GPIO_PD3_SSI2CLK);
    MAP_GPIOPinConfigure(GPIO_PD1_SSI2XDAT0);
    MAP_GPIOPinConfigure(GPIO_PD0_SSI2XDAT1);
    MAP_GPIOPinTypeSSI(GPIO_PORTD_BASE, GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_3 );
    ili9341_enable();
    // Config QSSI1 module, master mode, 16bits len.
    MAP_SSIDisable(SSI2_BASE);
    SSIClockSourceSet(SSI2_BASE, SSI_CLOCK_SYSTEM);
    MAP_SSIConfigSetExpClk(SSI2_BASE, systemClkFreq, SSI_FRF_MOTO_MODE_0,SSI_MODE_MASTER,30000000, 16);
    MAP_SSIEnable(SSI2_BASE);
}

void spi2_data_len(uint32_t len){
    MAP_SSIDisable(SSI2_BASE);
    MAP_SSIConfigSetExpClk(SSI2_BASE, systemClkFreq, SSI_FRF_MOTO_MODE_0,SSI_MODE_MASTER,30000000, len);
    MAP_SSIEnable(SSI2_BASE);
}

// uDMA //
void uDMA_spi2_config(void){
    // Enable uDMA peripheral
    MAP_SysCtlPeripheralEnable(SYSCTL_PERIPH_UDMA);
    while(!MAP_SysCtlPeripheralReady(SYSCTL_PERIPH_UDMA));
    // Enable uDMA controller
    MAP_uDMAEnable();
    // Set base address of control table
    MAP_uDMAControlBaseSet(uDMAControlTable);
    // Enable uDMA on SSI1 TX
    MAP_SSIDMAEnable(SSI2_BASE, SSI_DMA_TX);
    // Optional but safe
    MAP_uDMAChannelAssign(UDMA_CH13_SSI2TX);
    // Disable channel before configuration
    MAP_uDMAChannelDisable(UDMA_CH13_SSI2TX);
    // Configure channel
    MAP_uDMAChannelControlSet(UDMA_CH13_SSI2TX | UDMA_PRI_SELECT,
        UDMA_SIZE_16 |        // 16-bit data
        UDMA_SRC_INC_16 |     // increment source
        UDMA_DST_INC_NONE |   // SSI FIFO
        UDMA_ARB_8);          // burst of 8 words
    // Enable uDMA ints
}

void uDMA_spi2_send_buffer(uint16_t* dataBuffer, uint32_t bufferLen){
    // Disable channel before setup
    MAP_uDMAChannelDisable(UDMA_CH13_SSI2TX);
    // Configure transfer
    MAP_uDMAChannelTransferSet(
        UDMA_CH13_SSI2TX | UDMA_PRI_SELECT,
        UDMA_MODE_BASIC,
        dataBuffer,
        (void *)(SSI2_BASE + SSI_O_DR),
        bufferLen
    );
    // Start transfer
    MAP_uDMAChannelEnable(UDMA_CH13_SSI2TX);
}

// Funciones de apoyo
void intToStr(int32_t value, char *buf){
    char tmp[12];
    int i = 0, j = 0;

    if (value == 0)
    {
        buf[0] = '0';
        buf[1] = '\0';
        return;
    }

    if (value < 0)
    {
        buf[j++] = '-';
        value = -value;
    }

    while (value > 0)
    {
        tmp[i++] = (value % 10) + '0';
        value /= 10;
    }

    while (i > 0)
        buf[j++] = tmp[--i];

    buf[j] = '\0';
}

void floatToStr(float value, char *buf, uint8_t decimals){
    int32_t intPart;
    float frac;
    int i = 0,j = 0;

    if (value < 0)
    {
        buf[i++] = '-';
        value = -value;
    }

    intPart = (int32_t)value;
    frac = value - intPart;

    // Integer part
    char intBuf[12];
    intToStr(intPart, intBuf);

    for (j = 0; intBuf[j]; j++)
        buf[i++] = intBuf[j];

    buf[i++] = '.';

    // Fractional part
    while (decimals--)
    {
        frac *= 10.0f;
        int digit = (int)frac;
        buf[i++] = digit + '0';
        frac -= digit;
    }

    buf[i] = '\0';
}
