int main(void){
    //***** Clock config to run at 120 MHz *****//
    systemClkFreq = MAP_SysCtlClockFreqSet((SYSCTL_XTAL_25MHZ | SYSCTL_OSC_MAIN | SYSCTL_USE_PLL | SYSCTL_CFG_VCO_240), 120000000);
    //***** Invoque configuration functions *****//
    // Floating point unit enabling //
    MAP_FPUEnable();
    MAP_FPULazyStackingEnable();
    // Buttons //
    buttons_config();
    // Leds //
    leds_config();
    // Spi1 //
    spi0_config();
    // uDMA //
    uDMA_spi0_config();
    // Global ints enabling //
    MAP_IntMasterEnable();
    // Init display //
    ili9341_init();
    ili9341_fill_screen(BLACK);
    ili9341_print_string(0, 0, "TivaC : TM4C1294NCPDT", RED, BLACK);
    ili9341_print_string(0, 16, "2.4inch SPI Display : ILI9341", RED, BLACK);

    //***** Loop Forever *****//
    while(1){
        if(sw1State){ // SW1 pressed
            sw1State = 0x0;
            // Fill screen on Blue
            ili9341_fill_screen(BLUE);
        }
        if(sw2State){// SW2 pressed
            sw2State = 0x00;
            // Fil screen on Green
            ili9341_fill_screen(GREEN);
        }
    }
}

